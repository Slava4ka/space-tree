export interface Node {
  id: string;
  position: { x: number; y: number; z: number };
  title: string;
  description: string;
  color?: string;
}

const GRID_SIZE = 10;
const SPACING = 12;

const titles = [
  "Персонализированная медицина",
  "Энергоэффективность",
  "Глобальное управление",
  "Цифровые технологии",
  "Национальная безопасность",
  "Освоение территорий",
  "Экология и ресурсы",
  "Продовольственная безопасность",
  "Инновации",
  "Космические технологии",
  "Биотехнологии",
  "Нанотехнологии",
  "Квантовые вычисления",
  "Искусственный интеллект",
  "Робототехника",
];

const descriptions = [
  "Высокотехнологичное здравоохранение\nИнновационные методы лечения",
  "Новые источники энергии\nСохранение ресурсов",
  "Международные отношения\nТрансформация миропорядка",
  "Искусственный интеллект\nНовые материалы",
  "Противодействие угрозам\nИнформационная безопасность",
  "Связанность регионов\nАрктика и Антарктика",
  "Эффективное использование\nПриродные ресурсы",
  "Рациональное агрохозяйство\nАквакультура",
  "Научные разработки\nТехнологический прорыв",
  "Исследование космоса\nСпутниковые технологии",
  "Генная инженерия\nМолекулярная биология",
  "Наноматериалы\nМикроэлектроника",
  "Квантовые компьютеры\nКвантовая связь",
  "Машинное обучение\nНейронные сети",
  "Автоматизация\nПромышленные роботы",
];

export const nodes: Node[] = [];

for (let row = 0; row < GRID_SIZE; row++) {
  for (let col = 0; col < GRID_SIZE; col++) {
    const x = (col - (GRID_SIZE - 1) / 2) * SPACING;
    const y = ((GRID_SIZE - 1) / 2 - row) * SPACING;
    const z = 0;
    const id = `${row * GRID_SIZE + col + 1}`;
    const titleIndex = (row * GRID_SIZE + col) % titles.length;
    const descIndex = (row * GRID_SIZE + col) % descriptions.length;

    nodes.push({
      id,
      position: { x, y, z },
      title: titles[titleIndex],
      description: descriptions[descIndex],
    });
  }
}
